package com.hudl.workout.datacontainer;

import java.io.Serializable;

public class WorkoutConstraints implements Serializable {
	private int zone;
	private String description;
	private int minReps;
	private int maxReps;
	private int minRepTime;
	private int maxRepTime;
	private int targetRPM;
	private int minPower;
	private int maxPower;
	private int minHR;
	private int maxHR;
	private double restRatio;
	private int maxSetTimePerWorkout;
	
	public int getZone() {
		return zone;
	}
	public void setZone(int zone) {
		this.zone = zone;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getMinReps() {
		return minReps;
	}
	public void setMinReps(int minReps) {
		this.minReps = minReps;
	}
	public int getMaxReps() {
		return maxReps;
	}
	public void setMaxReps(int maxReps) {
		this.maxReps = maxReps;
	}
	public int getMinRepTime() {
		return minRepTime;
	}
	public void setMinRepTime(int minRepTime) {
		this.minRepTime = minRepTime;
	}
	public int getMaxRepTime() {
		return maxRepTime;
	}
	public void setMaxRepTime(int maxRepTime) {
		this.maxRepTime = maxRepTime;
	}
	public int getTargetRPM() {
		return targetRPM;
	}
	public void setTargetRPM(int targetRPM) {
		this.targetRPM = targetRPM;
	}
	public int getMinPower() {
		return minPower;
	}
	public void setMinPower(int minPower) {
		this.minPower = minPower;
	}
	public int getMaxPower() {
		return maxPower;
	}
	public void setMaxPower(int maxPower) {
		this.maxPower = maxPower;
	}
	public int getMinHR() {
		return minHR;
	}
	public void setMinHR(int minHR) {
		this.minHR = minHR;
	}
	public int getMaxHR() {
		return maxHR;
	}
	public void setMaxHR(int maxHR) {
		this.maxHR = maxHR;
	}
	public double getRestRatio() {
		return restRatio;
	}
	public void setRestRatio(double restRatio) {
		this.restRatio = restRatio;
	}
	public int getMaxSetTimePerWorkout() {
		return maxSetTimePerWorkout;
	}
	public void setMaxSetTimePerWorkout(int maxSetTimePerWorkout) {
		this.maxSetTimePerWorkout = maxSetTimePerWorkout;
	}
}
